namespace Magestore\Giftvoucher\Model\Source\Giftvoucher;

/**
 * Class StoresOptions
 * @package Magestore\Giftvoucher\Model\Source\Giftvoucher
 */
class StoresOptions implements \Magento\Framework\Data\OptionSourceInterface
{
    /**
     * @var \Magento\Store\Model\System\Store
     */
    private $store;

    /**
     * @param \Magento\Store\Model\System\Store $store
     */
    public function __construct(\Magento\Store\Model\System\Store $store)
    {
        $this->store = $store;
    }

    /**
     * @return array
     */
    public function toOptionArray()
    {
        return $this->store->getStoreValuesForForm(false, true);
    }
}
